    private void fetch() {
        int i;
        try {
            pst = con.prepareStatement("SELECT* FROM aluno JOIN turma ON aluno.fk_idTurma=turma.idTurma JOIN classe ON aluno.fk_idClasse= classe.idClasse JOIN funcionario on turma.fk_idProfessor=funcionario.idFuncionario");
            rs = pst.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            i = rsm.getColumnCount();
            DefaultTableModel df = (DefaultTableModel) jTableMatriculas.getModel();
            df.setRowCount(0);
            while (rs.next()) {
                Vector v2 = new Vector();
                for (int j = 1; j <= i; j++) {
                    v2.add(rs.getString("nome_aluno"));
                    v2.add(rs.getString("genero"));
                    v2.add(rs.getString("num_bilhete"));
                    v2.add(rs.getString("numMatricula"));
                    v2.add(rs.getString("nomeClasse"));
                    v2.add(rs.getString("nomeTurma"));
                    v2.add(rs.getString("nomeFuncionario"));

                }
                df.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Diretoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

