    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        Random ra = new Random();
        int numeroMatricula = ra.nextInt(999999 - 10000 + 1) + 10000;
        txtNumeroMatricula.setText(Integer.toString(numeroMatricula));
        Aluno aluno = new Aluno();
        try {
            aluno.setNome(txtNomeAluno.getText());
            aluno.setTelefone(txtTelefoneEncarregado.getText());

            if (rbdGeneroMasculino.isSelected()) {
                aluno.setGenero(rbdGeneroMasculino.getText());
            } else if (rbdGeneroFemenino.isSelected()) {
                aluno.setGenero(rbdGeneroFemenino.getText());
            }
            aluno.setNumeroBI(txtNumeroBI.getText());
            aluno.setClasse(txtIdClasse.getText());
            aluno.setTurma(txtIdTurma.getText());

            // Insert da tabela  aluno
            pst = con.prepareStatement("INSERT INTO `aluno`(`nome_aluno`, `num_bilhete`, `genero`, `numMatricula`, `fk_idTurma`, `fk_idClasse`) VALUES (?,?,?,?,?,?)", PreparedStatement.RETURN_GENERATED_KEYS);
            pst.setString(1, aluno.getNome());
            pst.setString(2, aluno.getNumeroBI());
            pst.setString(3, aluno.getGenero());
            pst.setString(4, Integer.toString(numeroMatricula));
            pst.setString(5, aluno.getTurma());
            pst.setString(6, aluno.getClasse());
           pst.executeUpdate();
            
            pst = con.prepareStatement("INSERT INTO `classe_turma` ( `fk_idTurma`, `fk_idClasse`) VALUES ( ?, ?)");
            pst.setString(1, aluno.getTurma());
            pst.setString(2, aluno.getClasse());
            int classe_turmaRowsAffected = pst.executeUpdate();
            if (classe_turmaRowsAffected  == 1) {
                JOptionPane.showMessageDialog(this, "Executado");

            }

        } catch (SQLException ex) {
            Logger.getLogger(Diretoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

